/**
 * Перевіряє, чи одинакові паролі
 * @return true або false
 */
    public boolean isPasswordsEquals() {

        if (txtPass.getText().equals(txtRepeat.getText())) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Паролі не співпадають!");
            return false;
        }

    }

    /**
     * Якщо пароля одинакові, то змінюється master-пароль
     * @param evt 
     */
    private void btnokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnokActionPerformed

        if (txtPass.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this, "Введіть будь ласка master-пароль!");
        }
        if (isPasswordsEquals()) {
            String pass = func.escaping(txtPass.getText());
            pass = Encryptor.encrypt(pass);
            func.updateMasterPassword(pass);
            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_btnokActionPerformed

